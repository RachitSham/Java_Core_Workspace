// This Program Demonstrate to Logical AND Operator..//

public class Logical_AND_Demo
{
	public static void main(String[] args)
	{
	    int r = 6;
		int h = 50;
		
		boolean b = (r > 3 && h < 30);
		
		// returns false because 6 is greater than 3 AND 50 is also greater than 30.

		System.out.println(b);
		
	}
}